/// <summary>
/// Enumeration of available texture resolution options for textures. <br/>
/// Each option corresponds to a specific texture resolution in pixels.
/// </summary>
public enum TextureResolutionOptions
{
    /// <summary>
    /// 256 x 256 pixels texture resolution.
    /// </summary>
    _256p = 256,

    /// <summary>
    /// 512 x 512 pixels texture resolution.
    /// </summary>
    _512p = 512,

    /// <summary>
    /// 1024 x 1024 pixels texture resolution.
    /// </summary>
    _1024p = 1024,

    /// <summary>
    /// 2048 x 2048 pixels texture resolution.
    /// </summary>
    _2048p = 2048
}